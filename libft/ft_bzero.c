#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*cpy;
	int		j;

	j = n;
	cpy = s;
	i = 0;
	if (n != '\0')
	{
		while (i < j)
		{
			cpy[i] = '\0';
			i++;
		}
	}
}
